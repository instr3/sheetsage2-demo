# 以 "Day X" 分段
days = re.split(r"Day\s+\d+", text)[1:]  # 第0段是空的

def clean(s):
    # 去掉 标点、数字、空白
    return re.sub(r"[0-9\s\W]+", "", s, flags=re.UNICODE)
if __name__ == '__main__':

    for i, block in enumerate(days, 1):
        cleaned = clean(block)
        positions = [idx+1 for idx, ch in enumerate(cleaned) if ch == "晨"]
        print(f"Day {i} 的“晨”位置：", [chr(pos + 64 - 1) for pos in positions])
